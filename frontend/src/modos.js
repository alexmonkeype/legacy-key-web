export const finalizar= async () => {
    
    const amount = document.getElementById("CantCustodia").value;            
    const tiempo = document.getElementById("tiempo").value;
    let valor;
    
    try{
        const transact= await window.contract1.methods.newMember(hijos,validadores,BigInt(amount).toString(),tiempo).call({ from: account });
        valor= Object.values(transact).length;
    } catch(e){
        if(e.message.toString().includes("invalid")){
            const error=e.message.toString();
            document.getElementById("error1").innerHTML="";               
            document.getElementById("error2").innerHTML=error;

        }else{
            const error=e.message.toString();
            const msg=JSON.parse(error.substring(error.indexOf("\n") + 1)).message;    
            document.getElementById("error1").innerHTML="";               
            document.getElementById("error2").innerHTML=msg;
        }               
    }

    if(valor==0){
        await window.contract1.methods.newMember(hijos,validadores,BigInt(amount).toString(),tiempo).send({ from: account });
        document.getElementById("mensaje2").innerHTML = "FELICIDADES YA ES MIEMBRO DE LA FAMILIA LEGACYKEY";
        document.getElementById("error1").innerHTML="At this moment everything is in order.";               
        document.getElementById("error2").innerHTML="";
    }
}  

